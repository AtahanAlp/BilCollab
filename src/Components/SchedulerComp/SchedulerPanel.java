        day = day.toUpperCase();
        
        dayInt = switch (day) {
            case "MONDAY" -> 1;
            case "TUESDAY" -> 2;
            case "WEDNESDAY" -> 3;
            case "THURSDAY" -> 4;
            case "FRİDAT" -> 5;
            case "SATURDAY" -> 6;
            case "SUNDAY" -> 7;
            default -> 1;
        };
          
        String startTime = jTextField_StartTime.getText();
        int startInt = Integer.parseInt(startTime);
        
        String endTime = jTextField_EndTime.getText();
        int endInt = Integer.parseInt(endTime);
        if(endInt == 0){
            endInt = 24;
        }
        
        String name = jTextField_Name.getText();
        
        Plan p = new Plan(name,dayInt,startInt,endInt);
        plans.add(p);
        
        
        for(int i = startInt; i < endInt; i++){
            jTable_Scheduler.setValueAt(name, i - 8, dayInt);
            //Object value = jTable_Scheduler.getValueAt(i - 8, dayInt);
            //value.setForeGround(Color.PINK);   
        }
        
